import {Skeleton} from 'vue-loading-skeleton';

import DoughnutChart from "~/components/DoughnutChart";
import BarChart from "~/components/BarChart";
import LineChart from "~/components/LineChart";
import 'vue2-datepicker/index.css';
import DatePicker from 'vue2-datepicker';
import moment from "moment";

export default {
  name: "index",
  components: {DoughnutChart, BarChart, LineChart, DatePicker, Skeleton},
  data: () => ({
    showChart: false,
    chartKey: 1,
    refreshKey: 1,
    user: "Select User",
    machine: "Select Machine",
    startDate: "",
    endDate: "",
    chartOption: {
      responsive: true,
      maintainAspectRatio: false,
      scales: {
        xAxes: [{
          ticks: {
            autoSkip: false,
            maxRotation: 90,
            minRotation: 90
          }
        }]
      },
    },
    generatingChart: false,
    generatingData: false,
    result: [],
  }),
  computed: {
    chartDatasets() {
      const datasets = [];
      const labels = [];
      this.result.map(row => {
        labels.push(row.Time);
      });

      const colorMap = {
        MainMotorCurrent: 'rgb(75, 192, 192)',
        Depth: 'rgb(255, 99, 132)',
      };

      ['MainMotorCurrent', 'Depth'].map(label => {
        datasets.push({
          label,
          backgroundColor: 'transparent',
          borderColor: colorMap[label],
          data: this.result.map(row => row[label]),
        })
      });
      return {datasets, labels};
    },
    users() {
      return this.$store.state.users;
    },
    machines() {
      return this.$store.state.machines;
    },
    userMachines() {
      return this.$store.state.machines.find(x => x.mobile === this.user)?.machines ?? [];
    }
  },
  mounted() {
    this.startDate = moment().format('YYYY-MM-DD');
    this.endDate = moment().format('YYYY-MM-DD');
    setInterval(() => {
      this.refreshKey += 1;
    }, 3000);
  },
  methods: {
    getData() {
      const payload = {
        "start": this.startDate + " 00:00:00",
        "end": this.endDate + " 00:00:00",
        "mobile": this.user,
        "ip": this.machine
      }
      return this.$axios.post('/exportData/', payload).then(data => {
        return data.data?.result ?? [];
      })
    },
    generateChart() {
      this.showChart = true;
      this.generatingChart = true;
      this.getData().then((data) => {
        if (data.length > 0) {
          this.result = data;
          this.chartKey = Math.random();
        } else {
          alert("No Data Found");
        }
        this.generatingChart = false;
      });
    },
    downloadData() {
      this.generatingData = true;
      this.showChart = false;
      this.getData().then((data) => {
        if (data.length > 0) {
          const array = [Object.keys(data[0])].concat(data)
          const hiddenElement = document.createElement('a');
          hiddenElement.href = 'data:text/csv;charset=utf-8,' +
            encodeURI(
              array.map(it => {
                return Object.values(it).toString()
              }).join('\n'));
          hiddenElement.target = '_blank';
          hiddenElement.download = 'output.csv';
          hiddenElement.click();
          hiddenElement.remove();
        } else {
          alert("No Data Found");
        }
        this.generatingData = false;
      });


    },
  },
  watch: {
    user() {
      this.machine = "Select Machine";
    }
  }
}

